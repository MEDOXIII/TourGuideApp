package com.example.android.tourguideapp;

public class Site {

    private String mSiteName;

    private int mSitepic;


    public Site(String siteName, int sitepic) {

        mSiteName = siteName;

        mSitepic = sitepic;


    }


    public String getmSiteName() {
        return mSiteName;
    }

    public int getmSitepic() {
        return mSitepic;
    }
}


